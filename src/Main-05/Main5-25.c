#include<stdio.h>
void main(){
	// 0x11은 00010001   128 64 32 16 8421 해서 =17
	// 0x11이 0001 0001로 나오는이유
	// 16진수는 4개자리 띄어서 변환하는데, 
	// 0001 위에칸 1의자리가 10이 됨.
	// 0001 따라서 11이 된다. (ex: 12는 00010010)
	int x=0x11;
	int y,z;
	// 0x = 0000, 0f = 1111 , f(=15)가 1111 = 00001111
	//   00010001
	//   00001111
	// = 1
	y=x& 0x0f;
	//   00010001
	//   00001111
	// = 31
	z=x|0x0f;
	// x= 17, y= 1, z=31
	printf("x=%d, y=%d, z=%d", x,y,z);
}