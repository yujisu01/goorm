#include<stdio.h>
void main(){
	int num[4] = {1,2,3,4};
	int *pt=num;
	// 100->101번지로
	pt++;
	// 101번지에 5 집어넣고 ++되서 -> 102번지 디폴트
	// 1,5,3,4
	*pt++ = 5;
	// 102번지에 10 집어넣고 ++되서 -> 103번지 
	// 1,5,10,4
	*pt++ = 10;
	// 103번지->102번지
	pt--;
	// 102번지(=10) 에 20더하고 ++되서 -> 103번지
	// 1,5,30,4
	*pt++ += 20;
	printf("%d %d %d %d", num[0],num[1],num[2],num[3]);
}

