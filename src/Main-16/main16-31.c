#include<stdio.h>
void main(){
	// 2차원배열 등장
	// 행 대표주소가 있음
	// num[0]은 100번지, num[1]은 103번지임 (아래에 내가 그려놈)
	int num[2][3] = {{10,20,30},{40,50,60}};
	for(int i=0; i<2; i++){
		for(int j=0; j<3; j++){
			// num의 i행 j열 출력
			// 0행0열, 0행1열, 0행2열
			// 1행0열, 1행1열, 1행2열
			printf("%d,",num[i][j]);
		}
		printf("\n");
	}
}

// num[0] 100번지
// num[1] 103번지 

// 10(100)  20(101)  30(102)
// 40(103)  50(104)  60(105)