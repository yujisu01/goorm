#include<stdio.h>
// 개헷갈려
int main(){
	// 100, 101, 102
	int val1[3]={10,20,30};
	// 200, 201, 202
	int val2[3]={90,80,70};
	// 포인터배열
	// 100, 200
	int *ptr[2]={val1,val2};
	
	// *ptr[0]의 값 + 3 = 10+3 = 13
	printf("%d,",*ptr[0]+3);
	// ptr[0]+1 = 100+1 = 101의 값 = 20+3 = 23
	// * , +3 중,. 포인터 가리키는것부터 먼저 하기
	printf("%d,",*(ptr[0]+1)+3);
	// *ptr[1]의 값 + 7 = 90+7 = 97
	printf("%d,",*ptr[1]+7);
	// ptr[1]+2 = 200+2 =202번지의 값 = 70 + 7 = 77
	printf("%d",*(ptr[1]+2)+7);
	return 0;
}