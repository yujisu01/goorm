#include<stdio.h>
int main(void){
	struct point{
		int x;
		int y;
	};
	//(1) 
	// x[0] = 1 2
	// x[1] = 3 4
	// x[2] = 5 6 
	struct point x[3]={{1,2},{3,4},{5,6}};
	struct point *p;
	// (2) 	 	    x y
	// p[0] (100) = 1 2
	// p[1] (101) = 3 4
	// p[2] (102) = 5 6
	p=x;
	
	// (3)
	// 100번지+1= 101번지가 갖고있는 x값 => 3
	// 100번지+2= 102번지가 갖고있는 y값 => 6 
	// (나 1,2,3,4,5,6에 각각 주소값 부여해버려서 틀림;주의좀ㅠ)
	printf("%d, %d", (p+1)->x, (p+2)->y);
	
	return 0;
}